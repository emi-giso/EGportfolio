import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const projectsDir = path.resolve(__dirname, '../public/projects');
const outputFile = path.resolve(__dirname, '../src/i18n/mediaManifest.ts');

const supportedExtensions = ['.jpg', '.jpeg', '.png', '.webp', '.mp4', '.mov', '.MOV', '.webm'];

function getMediaFiles(dir) {
  if (!fs.existsSync(dir)) return [];
  const files = fs.readdirSync(dir)
    .filter(file => supportedExtensions.includes(path.extname(file)));
  console.log(`Found ${files.length} files in ${dir}`);
  return files;
}

function generateManifest() {
  if (!fs.existsSync(projectsDir)) {
    console.error(`Directory not found: ${projectsDir}`);
    return;
  }
  
  const folders = fs.readdirSync(projectsDir).filter(f => fs.statSync(path.join(projectsDir, f)).isDirectory());
  const manifest = {};

  folders.forEach(folder => {
    const fullDir = path.join(projectsDir, folder);
    const files = getMediaFiles(fullDir);
    
    // Sort files based on "cover" priority
    files.sort((a, b) => {
      const aLower = a.toLowerCase();
      const bLower = b.toLowerCase();
      
      const aIsCover = aLower === '_cover' || aLower.includes('cover');
      const bIsCover = bLower === '_cover' || bLower.includes('cover');

      if (aIsCover && !bIsCover) return -1;
      if (!aIsCover && bIsCover) return 1;
      
      // If both are covers, check for folder-specific cover (e.g. huinca_cover)
      if (aIsCover && bIsCover) {
        const folderLower = folder.toLowerCase();
        if (aLower.includes(folderLower)) return -1;
        if (bLower.includes(folderLower)) return 1;
      }

      return aLower.localeCompare(bLower);
    });

    // Final paths - Encode filenames for URL safety
    manifest[folder] = files.map(file => `/EGportfolio/projects/${folder}/${encodeURIComponent(file)}`);
  });

  const content = `// This file is auto-generated by scripts/update-media-manifest.js
export const mediaManifest: Record<string, string[]> = ${JSON.stringify(manifest, null, 2)};
`;

  const outputDir = path.dirname(outputFile);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(outputFile, content);
  console.log(`Media manifest generated with ${folders.length} projects.`);
}

generateManifest();
